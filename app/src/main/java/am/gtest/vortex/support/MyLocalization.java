    public static void saveNewLanguage(Context ctx, View view) {

        // Is the button now checked?
        boolean checked = ((RadioButton) view).isChecked();

        // Check which radio button was clicked
        switch(view.getId()) {
            case R.id.rbEnglish:
                if (checked) {
                    MyPrefs.setString(PREF_KEY_SELECTED_LANGUAGE, CONST_EN);
                }
                break;
            case R.id.rbGreek:
                if (checked) {
                    MyPrefs.setString(PREF_KEY_SELECTED_LANGUAGE, CONST_GR);
                }
                break;
            case R.id.rbArabic:
                if (checked) {
                    MyPrefs.setString(PREF_KEY_SELECTED_LANGUAGE, CONST_AR);
                }
                break;
        }

        setupLanguage(ctx);
    }
